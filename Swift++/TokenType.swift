import Foundation

enum TokenType: String {
    case int = "TT_INT"
    case float = "FLOAT"
    case plus = "PLUS"
    case minus = "MINUS"
    case multiply = "MUL"
    case divide = "DIV"
    case leftParen = "LPAREN"
    case rightParen = "RPAREN"
}
